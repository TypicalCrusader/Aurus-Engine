#pragma once
#include "types.h"

#define MAX_ENTRIES_OF_TYPE_IN_FILE = UINT16_MAX;

struct image {
    char * ID;
    u8 isCompressed;
    u8 isEncrypted;     
    u8 type; 
    u16 crc16;
    u32 entrysize;
    u8 data;
};

struct sound {
    char * ID;
    u8 isCompressed;    
    u8 isEncrypted;
    u8 type; 
    u16 crc16;  
    u32 entrysize;
    u8 data;            
};

struct text {
    char * ID;
    u8 isCompressed;    
    u8 isEncrypted; 
    u8 type;      
    u16 crc16;   
    u32 entrysize;
    u8 data;     
};

struct gp_data {
    char * ID;
    u8 isCompressed;
    u8 isEncrypted;  
    u8 type;       
    u16 crc16;  
    u32 entrysize;
    u8 data;      
};

struct header{
    u32  signature;     //4b - BINO
    u8   version;       //1b - 0 = ver. 1.0
    u8   biniontype;    //1b - Potential future use?
    u32  crc32;         //4b
};

struct file{
    struct header       *header;
    const  u16          imagecount;
    struct image        *imagedata;
    const  u16          soundcount;
    struct sound        *sounddata;
    const u16           textcount;    
    struct text         *textdata;
    const u16           gpdatacount;    
    struct gp_data      *generaldata;
};